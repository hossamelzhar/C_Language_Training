
#include "calculator.h"

signed int sum(signed number_1, signed number_2)
{
    signed result = 0;
    result = number_1 + number_2;

    return result;
}

signed int sub(signed number_1, signed number_2)
{
    signed result = 0;
    result = number_1 - number_2;

    return result;
}

signed int mul(signed number_1, signed number_2)
{
    signed result = 0;
    result = number_1 * number_2;

    return result;
}

signed int dev(signed number_1, signed number_2)
{
    signed result = 0;
    result = number_1 / number_2;

    return result;
}
